int ecp_test_parse(int verbose)
{
	int ret = 0;

	ecp_keypair ecp;

	ecp_keypair_init(&ecp);

	if (verbose != 0)
		printf("  EC private key load: ");

	ret = ecp_keypair_parse_key(&ecp, (const byte_t *)TEST_CA_KEY_EC_PEM, strlen(TEST_CA_KEY_EC_PEM), TEST_CA_PWD_EC_PEM, strlen(TEST_CA_PWD_EC_PEM));
	if (ret != 0)
	{
		if (verbose != 0)
			printf("failed\n");

		goto cleanup;
	}

	if (verbose != 0)
		printf("passed\n\n");

cleanup:
	ecp_keypair_free(&ecp);

	return(ret);
}

